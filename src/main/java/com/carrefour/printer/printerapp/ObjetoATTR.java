package com.carrefour.printer.printerapp;

public class ObjetoATTR {

  private String param = "";

  private String line = "";

  private String value = "";

  /**
   * @return param
   */
  public String getParam() {

    return this.param;
  }

  /**
   * @param param new value of {@link #getparam}.
   */
  public void setParam(String param) {

    this.param = param;
  }

  /**
   * @return line
   */
  public String getLine() {

    return this.line;
  }

  /**
   * @param line new value of {@link #getline}.
   */
  public void setLine(String line) {

    this.line = line;
  }

  /**
   * @return value
   */
  public String getValue() {

    return this.value;
  }

  /**
   * @param value new value of {@link #getvalue}.
   */
  public void setValue(String value) {

    this.value = value;
  }

}